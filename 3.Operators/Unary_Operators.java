public class Unary_Operators {
  public static void main(String[] args) {
    int a = 5;
    System.out.println(++a);
    System.out.println(a++);
    System.out.println(a);
    System.out.println(--a);
    System.out.println(a--);
    System.out.println(a);
    System.out.println(+a);
    System.out.println(-a);

    boolean flag = true;
    System.out.println(!flag);
  }
}


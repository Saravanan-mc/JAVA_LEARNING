public class Else_If_Statement {
    public static void main(String[] args) {
        int a = 10, c = 15;
        if(c > a)
            System.out.println("c is greater than a");
        else if(c == a)
            System.out.println("c is equal to a");
        else
            System.out.println("c is smaller than a");
    }
}
